package com.escanor.shiro.service.impl;

import com.escanor.shiro.dao.UserInfoDao;
import com.escanor.shiro.dto.UserInfoDto;
import com.escanor.shiro.entity.UserInfoEntity;
import com.escanor.shiro.service.UserInfoService;
import org.modelmapper.ModelMapper;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

@Service
public class UserInfoServiceImpl implements UserInfoService {

    @Autowired
    private UserInfoDao userInfoDao;

    @Override
    public UserInfoEntity  findByUserName(String userName) {
        return userInfoDao.findByUserName(userName);
    }
}
